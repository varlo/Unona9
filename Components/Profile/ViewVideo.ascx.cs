    /// <summary>
    ///		Summary description for ViewVideo.
    /// </summary>
    public partial class ViewVideo : UserControl
    {
        protected VideoRecorder VideoRecorder1;

        private User user;

        public User User
        {
            set
            {
                user = value;
                ViewState["Username"] = user != null ? user.Username : null;
            }
            get
            {
                if (user == null
                    && ViewState["Username"] != null)
                    user = User.Load((string)ViewState["Username"]);
                return user;
            }
        }

        protected void Page_Load(object sender, EventArgs e)
        {

        }

        protected override void OnPreRender(EventArgs e)
        {
            base.OnPreRender(e);

            VideoRecorder1.strTargetUsername = User.Username;
            VideoRecorder1.bRender = true;
        }

